    self.replace("0", "zero")
    self.replace("1", "one")
    self.replace("2", "two")
    self.replace("3", "three")
    self.replace("4", "four")
    self.replace("5", "five")
    self.replace("6", "six")
    self.replace("7", "seven")
    self.replace("8", "eight")
    self.replace("9", "nine")